package com.yash.beveragesEntities;

public class BlackTea {
	public Integer teaPowderQuantity;
	public Integer waterQuantity;
	public Integer sugarQuantity;

	

	public BlackTea(Integer teaPowderQuantity, Integer waterQuantity, Integer sugarQuantity) {
		super();
		this.teaPowderQuantity = teaPowderQuantity;
		this.waterQuantity = waterQuantity;
		this.sugarQuantity = sugarQuantity;
	}

	

}
